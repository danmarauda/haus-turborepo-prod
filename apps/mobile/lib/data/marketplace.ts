// Helper functions
export function getCategoryBySlug(slug: string): ServiceCategoryInfo | undefined {
  return serviceCategories.find((cat) => cat.id === slug);
}

export function getProviderBySlug(slug: string): ServiceProvider | undefined {
  return featuredProviders.find((p) => p.slug === slug);
}

export function getProvidersByCategory(categoryId: ServiceCategory): ServiceProvider[] {
  return featuredProviders.filter((p) => p.category === categoryId);
}

export function getReviewsByProvider(providerId: string): Review[] {
  return reviews.filter((r) => r.providerId === providerId);
}

export function getRecentReviews(limit: number = 5): Review[] {
  return [...reviews]
    .sort((a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime())
    .slice(0, limit);
}
